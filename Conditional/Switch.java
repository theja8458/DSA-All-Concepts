public class Switch {
    public static void main(String args[]){
        int number=2;
        switch(number){
            case 1: System.out.println("samosa");
            break;
            case 2: System.out.println("burger");
            break;
            case 3: System.out.println("Mango");
            break;
            default : System.out.println("Wake up");
        }
    }
    
}
